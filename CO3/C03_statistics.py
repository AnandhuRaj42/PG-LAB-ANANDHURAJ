import statistics

print("MEAN:",statistics.mean([2,4,7,6,10]))

print("MEDIAN:",statistics.median([1, 3, 5, 7, 9, 11, 13]))

print("MODE:",statistics.mode([1, 1, -3, 3, 7, -9]))

print("VARIENCE:",statistics.variance([1, 3, 5, 7, 9, 11]))

print("HARMONIC MEAN:",statistics.harmonic_mean([40, 60, 80]))
